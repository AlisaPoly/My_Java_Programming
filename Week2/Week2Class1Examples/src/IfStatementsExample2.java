public static void main(String[] args) {
    int age = 20;
    if (age < 4) {
        System.out.println("Baby");
    }
    else if (age < 13) {
        System.out.println("Child");
    }
    else if (age < 20) {
        System.out.println("Teenager");
    }
    else {
        System.out.println("Adult");
    }
}